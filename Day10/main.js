// Converting Strings to Numbers: Write a function that takes a string representation of a number
//  (e.g., `123`) and converts it into an actual number type.
function stringNumber(str) {
    return Number(str);
}
console.log(stringNumber("125"));
// Rounding Numbers: Create a function that takes a number with decimals (e.g., `3.14159`)
//  and rounds it to two decimal places.
function rouNumber(num) {
    return Math.round(num);
}
console.log(rouNumber(3.14159));
